const Featured = () => {
  const data = useStaticQuery(graphql`
    {
      featured: allMarkdownRemark(
        filter: { fileAbsolutePath: { regex: "/content/featured/" } }
        sort: { fields: [frontmatter___date], order: ASC }
      ) {
        edges {
          node {
            frontmatter {
              title
              overline
              cover {
                childImageSharp {
                  gatsbyImageData(width: 700, placeholder: BLURRED, formats: [AUTO, WEBP, AVIF])
                }
                publicURL
              }
              tech
              github
              external
            }
            html
          }
        }
      }
    }
  `);

  const featuredProjects = data.featured.edges.filter(({ node }) => node);
  const [modalGif, setModalGif] = useState(null);
  const revealTitle = useRef(null);
  const revealProjects = useRef([]);
  const prefersReducedMotion = usePrefersReducedMotion();

  useEffect(() => {
    if (prefersReducedMotion) {
      return;
    }

    sr.reveal(revealTitle.current, srConfig());
    revealProjects.current.forEach((ref, i) => sr.reveal(ref, srConfig(i * 100)));
  }, []);

  const handleKeyDown = useCallback(e => {
    if (e.key === 'Escape') {
      setModalGif(null);
    }
  }, []);

  useEffect(() => {
    if (modalGif) {
      document.addEventListener('keydown', handleKeyDown);
      document.body.style.overflow = 'hidden';
    }
    return () => {
      document.removeEventListener('keydown', handleKeyDown);
      document.body.style.overflow = '';
    };
  }, [modalGif, handleKeyDown]);

  return (
    <section id="projects">
      <h2 className="numbered-heading" ref={revealTitle}>
        Some Things Iâ€™ve Built
      </h2>

      <StyledProjectsGrid>
        {featuredProjects &&
          featuredProjects.map(({ node }, i) => {
            const { frontmatter, html } = node;
            const { external, title, tech, github, cover, overline } = frontmatter;
            const image = getImage(cover);
            const publicURL = cover?.publicURL;

            return (
              <StyledProject key={i} ref={el => (revealProjects.current[i] = el)}>
                <div className="project-content">
                  <div>
                    <p className="project-overline">{overline || 'Featured Project'}</p>

                    <h3 className="project-title">{title}</h3>

                    <div
                      className="project-description"
                      dangerouslySetInnerHTML={{ __html: html }}
                    />

                    {tech.length && (
                      <ul className="project-tech-list">
                        {tech.map((tech, i) => (
                          <li key={i}>{tech}</li>
                        ))}
                      </ul>
                    )}

                    <div className="project-links">
                      {github && (
                        <a href={github} aria-label="GitHub Link">
                          <Icon name="GitHub" />
                        </a>
                      )}
                      {external && (
                        <a href={external} aria-label="External Link" className="external">
                          <Icon name="External" />
                        </a>
                      )}
                    </div>

                    {publicURL && (
                      <StyledViewDemoButton onClick={() => setModalGif(publicURL)}>
                        Preview Project
                      </StyledViewDemoButton>
                    )}
                  </div>
                </div>

                <div className="project-image">
                  <a href={external ? external : github ? github : '#'}>
                    {image ? (
                      <GatsbyImage image={image} alt={title} className="img" />
                    ) : publicURL ? (
                      <img src={publicURL} alt={title} className="img" />
                    ) : null}
                  </a>
                </div>
              </StyledProject>
            );
          })}
      </StyledProjectsGrid>

      {modalGif && (
        <StyledModal onClick={() => setModalGif(null)}>
          <button className="modal-close" aria-label="Close modal">
            &#x2715;
          </button>
          <div role="presentation" onClick={e => e.stopPropagation()}>
            <img src={modalGif} alt="Project demo" />
          </div>
        </StyledModal>
      )}
    </section>
  );
};

export default Featured;
